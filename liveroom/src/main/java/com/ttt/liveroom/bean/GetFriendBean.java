public class GetFriendBean {
    String id;
    String f_name;
    String f_url;
    String f_pic;
    String f_addtime;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getF_name() {
        return f_name;
    }

    public void setF_name(String f_name) {
        this.f_name = f_name;
    }

    public String getF_url() {
        return f_url;
    }

    public void setF_url(String f_url) {
        this.f_url = f_url;
    }

    public String getF_pic() {
        return f_pic;
    }

    public void setF_pic(String f_pic) {
        this.f_pic = f_pic;
    }

    public String getF_addtime() {
        return f_addtime;
    }

    public void setF_addtime(String f_addtime) {
        this.f_addtime = f_addtime;
    }
}
